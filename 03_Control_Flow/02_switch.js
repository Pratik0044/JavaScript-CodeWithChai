const month=3;

switch (month){
    case 1:
        console.log("JANUARY");
    break;

    case 2:
        console.log("FEBRUARY");
    break;

    case 3:
        console.log("MARCH");
    break;

    case 4:
        console.log("APRILE");
    break;

    case 5:
        console.log("MAY");
    break;

    default:
        console.log("Invalid input: ");
    break;
}

